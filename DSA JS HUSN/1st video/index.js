const studentDatabase = ["Jorden", "Erick", "Lamon", "Guil", "Thomas"];

const findStudent = (StudentList, StudentName) => {
    for (let i = 0; i < StudentList.length; i++) {
        if (StudentList[i] === StudentName) {
            console.log(`Found ${StudentName} at ${i} number of index`)
        }
    }
}
